package com.kc.listener;

public class Listener {
	/*
	 * Listener 监听器
	 * 		专门用于对其他对象上发生的事件或状态改变进行监听和处理的对象
	 * 
	 * 	Servlet 监听器用于监听 WEB 应用程序中的 ServletContext，HttpSession，ServletRequest 等域对象的创建和销毁事件
	 * 		以及这些域对象中属性发生修改的事件
	 * 	
	 * 三类监听器：
	 * 		1.监听域对象自身的创建和销毁的事件监听器
	 * 		2.监听域对象中属性的增加和删除的事件监听器
	 * 		3.监听绑定到 HttpSession 域中的某个对象的状态的事件监听器
	 * 
	 * 
	 */
}
